//    Constructor (inside Class) = Builder: Creates and sets up the core structure of the house (object).
//    Main Method = Assembly/Finishing Touches: You assemble the house (create the object) and make it usable.

//    1. Define the Class  :  In a separate file or within a class file (usually at the top of your code).
//    2. Create the Constructor  :  Inside the class itself, alongside other methods and instance variables.
//    3. Create an Object  :  In the main method (or wherever you need to use the object).
//    4. Use the Object  :  Inside the main method or other methods to interact with the object.


//    Rule of thumb: Whenever you need an object to start with specific information or settings, you use a constructor.

//    Its job is only to initialize your object (set up name, age, weight, etc.).
